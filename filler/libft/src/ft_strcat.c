#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char *d;
	char *s;

	d = dest;
	s = (char*)src;
	while (*d != '\0')
		d++;
	while (*s != '\0')
	{
		*d = *s;
		d++;
		s++;
	}
	*d = '\0';
	return (dest);
}

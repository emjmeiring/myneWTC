#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
		return ((char*)s);
	while (*s++ != '\0')
	{
		if (*s == (unsigned char)c)
			return ((char*)s);
	}
	return (NULL);
}
